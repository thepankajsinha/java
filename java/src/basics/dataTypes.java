package basics;

public class dataTypes {
    public static void main(String[] args) {
        // ----- Primitive Data Types -----

        // byte (1 byte)
        byte b = 100;

        // short (2 bytes)
        short s = 20000;

        // int (4 bytes)
        int i = 100000;

        // long (8 bytes)
        long l = 10000000000L; // must use L

        // float (4 bytes)
        float f = 12.34f; // must use f

        // double (8 bytes)
        double d = 123456.789;
        System.out.println("double: " + d);

        // char (2 bytes, Unicode)
        char c = 'A';

        // boolean (1 bit)
        boolean flag = true;
    }
}
